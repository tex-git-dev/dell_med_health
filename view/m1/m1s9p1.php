  }
}

 function onUpdate(v){
     if(aud.currentTime == aud.duration){
         aud.pause();
         aud.currentTime = 0;
         $("#s9ad").removeClass('fa-pause-circle-o').addClass('fa-play-circle-o');  
         $(".pText").text('PLAY NOW')  
     }
 }





