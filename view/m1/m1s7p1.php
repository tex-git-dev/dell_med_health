         <f><h3>CASE STUDY</h3></f>    
         <br>     
         <f><p>In seeking to improve care value, a central challenge that most health care delivery organizations face is their limited capacity to measure and analyze health care value, particularly around costs. In 2012, University of Utah Health Care, under the leadership of Dr. Vivian Lee, initiated a large-scale effort to create a "value-driven outcomes" (VDO) tool that would provide clinicians and managers the ability to analyze actual system costs and patient measures.<sup>1</sup> </p></f>
         <f><p>As we examine the following case, think about how to measurably improve value at the scale of an entire health care system and how to use data to help identify potential sources for improvement. </p></f>

         <f><h4 class="text-uppercase">Defining Outcomes</h4></f>
         <div class="row">
             <div class="col-sm-6"> 
              <f><p>The University of Utah Health Care system, as of November 2015, included 4 hospitals, 11 community clinics, 15 regional partners, and 1,380 physicians, and saw approximately 1.4 million patients each year. First, The University of Utah had to determine measures for their patient outcomes, processes of care, patient experience, and costs. Each of these metrics can be difficult to precisely define, and we will explore them in more depth later in this course. For now, let’s focus on the bigger picture of how defining, measuring, and evaluating aspects of care delivery is critical to improving value. </p></f>
              <f><p>To define outcomes, clinical teams consisting of physicians, nurses, administrators, and quality improvement staff collaborated to identify clinically relevant outcomes that consider patient priorities. These included risk-adjusted mortality, patient safety measures such as hospital-acquired infections, clinical process measures, and unplanned hospital readmission or emergency department visits. They also collected patient satisfaction data.</p></f>
              <f><p>The care team for each specialty selected key quality and outcome variables that they felt were most important to the care of their specific patients. These were combined into a single binary measure that they called “perfect care.” To achieve perfect care, the care team had to accomplish ALL of the key elements. </p></f>                            
              <f><h4 class="text-uppercase">Determining Costs</h4></f>
              <f><p>Next, the University of Utah Health Care system had to determine how much everything in the hospital actually cost.  All hospitals have a “charge master,” which lists the charges for each item or procedure that they provide. Few, however, have a list that shows what these items actually COST. An important concept to note is that the price does NOT equal the actual cost. The University of Utah dedicated significant resources to study and define how much processes and items within their hospitals cost, and they referred to their database of these costs as a “cost master.”</p></f>
