                  <f><p>The costs of health care are way too high given the outcomes now achieved. Overuse, underuse and inappropriate use of care cumulate to stunning amounts of wasteful spending that drive costs up. Costs are a major problem with our health care system today, not only at the individual patient level, but at the level of organizations as well. This course focuses on how professionals can influence and change spending. Costs, how they cumulate and how they relate to prices that patients pay, are complex and often opaque. In this module, you will learn how costs occur and how this differs from how costs are assessed and tallied. You will see three methods of cost assessment and consider different types of physician reimbursement. You will also consider which methods provide the most transparency. Through gaining an understanding of and ability to discuss health care costs, you will be armed to provide clarity to your patients about the expense they may face from unnecessary services.</p></f>
